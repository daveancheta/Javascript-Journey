// The sort() method sorts an array alphabetically:
const fruits = ["Dragonfruit", "Cantaloupe", "Banana", "Apple", "Elderberry"];
fruits.sort();
console.log(fruits); // Output: [ 'Apple', 'Banana', 'Cantaloupe', 'Dragonfruit', 'Elderberry' ]

// Sort and reverse the array.
const fruits2 = ["Dragonfruit", "Cantaloupe", "Banana", "Apple", "Elderberry"];
fruits2.sort().reverse();
console.log(fruits2); // Output: [ 'Apple', 'Banana', 'Cantaloupe', 'Dragonfruit', 'Elderberry' ]